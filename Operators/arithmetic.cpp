#include <iostream>
using namespace std;
int main()
{
    int a = 10, b = 5;

    cout << "sum = " << (a + b) << endl;
    cout << "difference = " << (a - b) << endl;
    cout << "product = "<< (a * b) << endl; 
    cout << "division = " << (a / b) << endl; // 2
    cout << "modulo = "<< (a % b) << endl; //0 

    return 0;
} 