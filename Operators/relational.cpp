#include <iostream>
using namespace std;
int main()
{
    int a = 3, b = 6;

    cout << "a<b "<< (a < b) << endl; // True
    cout << "a>b "<< (a > b) << endl; // false
    cout << "a>=b "<< (a <= b) << endl;  // true
    cout << "a>=b "<< (a >= b) << endl; // false
    cout << "a==b "<< (a == b) << endl;  // false
    cout << "a!=b "<< (a != b) << endl;  // true

    return 0;
}